export function recommendProgram({ level, daysPerWeek }) {
  if (level === 'новичок') return 'fullbody'
  if (level === 'средний') {
    if (daysPerWeek <= 3) return 'split_synergist'
    if (daysPerWeek === 4) return 'upper_lower'
    return 'ppl'
  }
  // продвинутый
  if (daysPerWeek <= 3) return 'split_synergist'
  if (daysPerWeek === 4) return 'upper_lower'
  if (daysPerWeek === 5) return 'ppl'
  return 'bro_split'
}
