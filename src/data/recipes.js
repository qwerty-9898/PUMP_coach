// Подобрать идеи под остаток калорий и нехватку белка.
export function suggestRecipes(remainingKcal, proteinLeft, goal, limit = 6) {
  const cap = remainingKcal > 0 ? remainingKcal + 80 : 260
  let list = RECIPES.filter(r => r.kcal <= cap)
  if (!list.length) list = [...RECIPES].sort((a, b) => a.kcal - b.kcal).slice(0, limit)
  // нужен белок → белковые вперёд; иначе — ближе к остатку калорий
  if (proteinLeft > 20) list = list.sort((a, b) => b.p - a.p)
  else if (goal === 'похудение' || goal === 'тонус') list = list.sort((a, b) => a.kcal - b.kcal)
  else list = list.sort((a, b) => b.p - a.p)
  return list.slice(0, limit)
}
